/** Source enum mirrors the SQL CHECK on finished_lot_raw_bags / _packaging_lots. */
export type RecallSource =
  | "PROJECTOR"
  | "BACKFILL"
  | "MANUAL"
  | "LEGACY_IMPORT";

export type ContributingBagLink = {
  inventoryBagId: string;
  workflowBagId: string | null;
  quantityConsumedPills: number | null;
  confidence: RecallConfidence;
  source: RecallSource;
  derivedFromEventId: string | null;
};

// ─── Pure helpers (tested directly) ───────────────────────────────────

/** Decide what trace_code a finished_lots row should carry. Preserves
 *  an existing value; falls back to `FL-<finishedLotNumber>` for new
 *  / null trace codes. */
export function deriveTraceCodeForLot(args: {
  existingTraceCode: string | null;
  finishedLotNumber: string;
}): string {
  if (args.existingTraceCode && args.existingTraceCode.trim().length > 0) {
    return args.existingTraceCode;
  }
  return buildFinishedLotTraceCode({
    finishedLotNumber: args.finishedLotNumber,
  });
}

/** Snapshot for finished_lot_outputs.print_payload. Trace code +
 *  product name + packed date + optional customer alias. Never
 *  contains supplier_lot_number. */
export function buildPrintPayload(args: {
  traceCode: string;
  productName: string | null;
  productSku: string | null;
  packedAt: Date | null;
  expiresAt: Date | null;
  finishedLotCodeAlias: string | null;
}): Record<string, unknown> {
  const payload: Record<string, unknown> = {
    source: "PROJECTOR",
    schema_version: "1.0",
    trace_code: args.traceCode,
    product_name: args.productName,
    product_sku: args.productSku,
    packed_at: args.packedAt ? args.packedAt.toISOString() : null,
    expires_at: args.expiresAt ? args.expiresAt.toISOString() : null,
  };
  if (args.finishedLotCodeAlias) {
    payload.customer_alias = args.finishedLotCodeAlias;
  }
  return payload;
}

/** Project finished_lots counts into one finished_lot_outputs row per
 *  non-zero output type. Skips zero / null counts — we never
 *  fabricate. */
export function deriveOutputRows(args: {
  unitsProduced: number;
  displaysProduced: number | null;
  casesProduced: number | null;
  traceCode: string;
  printPayload: Record<string, unknown>;
}): Array<{
  outputType: "LOOSE_UNIT" | "DISPLAY" | "MASTER_CASE";
  quantity: number;
  unit: string;
  traceCodePrinted: string;
  printPayload: Record<string, unknown>;
}> {
  const rows: Array<{
    outputType: "LOOSE_UNIT" | "DISPLAY" | "MASTER_CASE";
    quantity: number;
    unit: string;
    traceCodePrinted: string;
    printPayload: Record<string, unknown>;
  }> = [];
  if (args.unitsProduced > 0) {
    rows.push({
      outputType: "LOOSE_UNIT",
      quantity: args.unitsProduced,
      unit: "each",
      traceCodePrinted: args.traceCode,
      printPayload: args.printPayload,
    });
  }
  if (args.displaysProduced != null && args.displaysProduced > 0) {
    rows.push({
      outputType: "DISPLAY",
      quantity: args.displaysProduced,
      unit: "each",
      traceCodePrinted: args.traceCode,
      printPayload: args.printPayload,
    });
  }
  if (args.casesProduced != null && args.casesProduced > 0) {
    rows.push({
      outputType: "MASTER_CASE",
      quantity: args.casesProduced,
      unit: "each",
      traceCodePrinted: args.traceCode,
      printPayload: args.printPayload,
    });
  }
  return rows;
}

/** Given the raw inputs the projector can see, build the set of
 *  contributing-bag links to upsert. */
export function deriveContributingBags(args: {
  workflowBagId: string | null;
  workflowBagInventoryBagId: string | null;
  workflowBagPillCount: number | null;
  /** Each row from finished_lot_inputs that points at a batch. */
  batchInputs: Array<{
    batchId: string;
    qtyConsumed: number;
    derivedFromEventId: string | null;
  }>;
  /** Bags that belong to the contributing batches. */
  bagsByBatch: Record<string, Array<{ id: string; pillCount: number | null }>>;
}): ContributingBagLink[] {
  const links: ContributingBagLink[] = [];

  // HIGH-confidence direct link from workflow_bag.inventory_bag_id.
  if (args.workflowBagId && args.workflowBagInventoryBagId) {
    links.push({
      inventoryBagId: args.workflowBagInventoryBagId,
      workflowBagId: args.workflowBagId,
      quantityConsumedPills: args.workflowBagPillCount,
      confidence: "HIGH",
      source: "PROJECTOR",
      derivedFromEventId: null,
    });
  }

  // LOW-confidence batch-level inference. For each batch input row,
  // fan out to every inventory_bag in that batch. We deliberately
  // don't try to split qtyConsumed across bags — better to leave the
  // per-bag quantity null than to fabricate.
  const seen = new Set(links.map((l) => keyOf(l.inventoryBagId, l.workflowBagId)));
  for (const inp of args.batchInputs) {
    const bagsInBatch = args.bagsByBatch[inp.batchId] ?? [];
    for (const bag of bagsInBatch) {
      const k = keyOf(bag.id, args.workflowBagId);
      if (seen.has(k)) continue;
      // Same (inventory_bag, workflow_bag) was already inserted at HIGH
      // confidence — don't downgrade.
      const kNullWorkflow = keyOf(bag.id, null);
      if (seen.has(kNullWorkflow)) continue;
      links.push({
        inventoryBagId: bag.id,
        workflowBagId: args.workflowBagId,
        quantityConsumedPills: null,
        confidence: "LOW",
        source: "LEGACY_IMPORT",
        derivedFromEventId: inp.derivedFromEventId,
      });
      seen.add(k);
    }
  }

  return links;
}

function keyOf(bagId: string, workflowBagId: string | null): string {
  return `${bagId}::${workflowBagId ?? "null"}`;
}

/** Tells callers how confident a finished_lot's passport is overall.
 *  MIN across all contributing links. Empty → MISSING. */
export function summarizePassportConfidence(
  links: ContributingBagLink[],
): RecallConfidence {
  return rollupRecallConfidence(links.map((l) => l.confidence));
}

