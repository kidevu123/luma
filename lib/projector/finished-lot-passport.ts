export type RebuildResult = {
  scanned: number;
  projected: number;
  skipped: number;
  totalRawBags: number;
  totalOutputs: number;
  totalPackagingLots: number;
  totalQcEvents: number;
};

/** Walk every finished_lots row and run the per-lot projection.
 *  Idempotent. Used by scripts/rebuild-read-models.ts. */
export async function rebuildFinishedLotPassport(
  tx: Tx,
): Promise<RebuildResult> {
  const lots = await tx
    .select({ id: finishedLots.id })
    .from(finishedLots);
  const result: RebuildResult = {
    scanned: lots.length,
    projected: 0,
    skipped: 0,
    totalRawBags: 0,
    totalOutputs: 0,
    totalPackagingLots: 0,
    totalQcEvents: 0,
  };
  for (const l of lots) {
    const r = await projectFinishedLotPassportForLot(tx, l.id);
    if (r.skipped) {
      result.skipped += 1;
      continue;
    }
    result.projected += 1;
    result.totalRawBags += r.rawBagsWritten;
    result.totalOutputs += r.outputsWritten;
    result.totalPackagingLots += r.packagingLotsWritten;
    result.totalQcEvents += r.qcEventsWritten;
  }
  return result;
}
