// LOT-1C — finished-lot / recall-passport projector.
//
// Hooks two upstream code paths:
//   1. BAG_FINALIZED (lib/projector/index.ts) — when a workflow_bag
//      finalizes AND a finished_lots row already exists for it,
//      backfill the recall-passport projection tables.
//   2. createFinishedLot (lib/db/queries/finished-lots.ts) — when an
//      operator manually creates a finished_lots row, immediately
//      project its passport children.
//
// Plus a full rebuilder (rebuildFinishedLotPassport) wired into
// scripts/rebuild-read-models.ts.
//
// Strict invariants per LOT-1A / LOT-1B and the LOT-1C prompt:
//   - Never auto-create finished_lots from BAG_FINALIZED alone.
//     The current operator-driven createFinishedLot flow stays as
//     the only "is this lot real" arbiter; this projector enriches
//     existing lots.
//   - Never fabricate output counts. unitsProduced / displaysProduced
//     / casesProduced come from finished_lots; we project rows only
//     when the underlying count > 0.
//   - Confidence ladder:
//       HIGH    — workflow_bag.inventory_bag_id directly identifies
//                 the raw bag.
//       LOW     — only finished_lot_inputs (batch level) is known;
//                 we fan out to the bags in that batch.
//       MISSING — neither chain yields anything; we skip.
//   - Preserve existing trace_code. Once a trace_code is set on a
//     finished_lot it is never re-generated by the projector — that's
//     the customer-facing identifier and operators may have
//     hand-edited it.
//   - Supplier lot is NEVER embedded in print_payload by default.
//     LOT-1F's outbound layer will gate exposure per customer flag.
//   - Idempotent: re-running the projector on the same finished_lot
//     produces the same row set. Children are upserted on their
//     natural unique keys; PROJECTOR-source outputs are DELETE+INSERT
//     so a re-run reflects the latest counts.

